#include "tests.hpp"

static void
TestOneInput(flexi_result_e ex_result, const std::vector<uint8_t> &data)
{
    flexi_buffer_s buffer = flexi_make_buffer(data.data(), data.size());
    flexi_cursor_s cursor{};

    flexi_result_e act_result = flexi_open_buffer(&buffer, &cursor);
    if (act_result != FLEXI_OK) {
        ASSERT_EQ(ex_result, act_result);
        return;
    }

    flexi_parser_s parser{
        [](const char *, void *) {},
        [](const char *, int64_t, void *) {},
        [](const char *, uint64_t, void *) {},
        [](const char *, float, void *) {},
        [](const char *, double, void *) {},
        [](const char *, const char *, void *) {},
        [](const char *, const char *, flexi_ssize_t, void *) {},
        [](const char *, flexi_ssize_t, void *) {},
        [](void *) {},
        [](const char *, flexi_ssize_t, void *) {},
        [](void *) {},
        [](const char *, const void *, flexi_type_e, int, flexi_ssize_t,
            void *) {},
        [](const char *, const void *, flexi_ssize_t, void *) {},
        [](const char *, bool, void *) {},
    };

    act_result = flexi_parse_cursor(&parser, &cursor, NULL);
    ASSERT_EQ(ex_result, act_result);
}

